import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { Badge } from "@/components/ui/badge";
import { TrendingUp, TrendingDown, Minus, AlertCircle } from "lucide-react";
import type { StockPrediction } from "@shared/schema";

interface PredictionPanelProps {
  prediction: StockPrediction;
}

export function PredictionPanel({ prediction }: PredictionPanelProps) {
  const predictionConfig = {
    BUY: {
      icon: TrendingUp,
      color: "text-gain",
      bgColor: "bg-gain/10",
      borderColor: "border-gain/30",
    },
    SELL: {
      icon: TrendingDown,
      color: "text-loss",
      bgColor: "bg-loss/10",
      borderColor: "border-loss/30",
    },
    HOLD: {
      icon: Minus,
      color: "text-muted-foreground",
      bgColor: "bg-muted",
      borderColor: "border-muted",
    },
  };

  const config = predictionConfig[prediction.prediction];
  const Icon = config.icon;

  return (
    <Card className={`shadow-lg border-2 ${config.borderColor}`}>
      <CardHeader>
        <div className="flex items-start justify-between gap-4">
          <div className="space-y-1">
            <CardTitle className="text-2xl">AI Prediction</CardTitle>
            <p className="text-sm text-muted-foreground">
              Based on technical analysis and market trends
            </p>
          </div>
          <Badge
            className={`${config.bgColor} ${config.color} text-lg px-4 py-2 font-bold`}
            variant="secondary"
          >
            <Icon className="h-5 w-5 mr-2" />
            {prediction.prediction}
          </Badge>
        </div>
      </CardHeader>
      <CardContent className="space-y-6">
        <div className="flex items-center gap-8">
          <div className="space-y-1">
            <p className="text-sm text-muted-foreground">Confidence Level</p>
            <div className="flex items-baseline gap-2">
              <span className="text-5xl font-bold">{prediction.confidence}%</span>
              <span className="text-muted-foreground">certainty</span>
            </div>
            <div className="w-full bg-muted h-2 rounded-full overflow-hidden mt-2">
              <div
                className={`h-full ${config.color === "text-gain" ? "bg-gain" : config.color === "text-loss" ? "bg-loss" : "bg-muted-foreground"}`}
                style={{ width: `${prediction.confidence}%` }}
              />
            </div>
          </div>
          <div className="space-y-1">
            <p className="text-sm text-muted-foreground">Target Price</p>
            <p className="text-3xl font-mono font-bold">
              ${prediction.targetPrice.toFixed(2)}
            </p>
          </div>
        </div>

        <div className="grid grid-cols-3 gap-4">
          <div className="space-y-1">
            <p className="text-sm text-muted-foreground">RSI</p>
            <p className="text-xl font-semibold">{prediction.metrics.rsi}</p>
          </div>
          <div className="space-y-1">
            <p className="text-sm text-muted-foreground">MACD</p>
            <p className="text-xl font-semibold">{prediction.metrics.macd}</p>
          </div>
          <div className="space-y-1">
            <p className="text-sm text-muted-foreground">Trend</p>
            <p className="text-xl font-semibold capitalize">{prediction.metrics.trend}</p>
          </div>
        </div>

        <div className="bg-muted/50 rounded-lg p-4">
          <p className="text-sm leading-relaxed">{prediction.reasoning}</p>
        </div>

        <div className="flex items-start gap-2 text-xs text-muted-foreground bg-muted/30 rounded-lg p-3">
          <AlertCircle className="h-4 w-4 mt-0.5 flex-shrink-0" />
          <p>
            This prediction is generated by AI analysis and should not be considered as financial
            advice. Always do your own research and consult with a financial advisor before making
            investment decisions.
          </p>
        </div>
      </CardContent>
    </Card>
  );
}
